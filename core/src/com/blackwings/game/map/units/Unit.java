package com.blackwings.game.map.units;

abstract public class Unit {
    protected float x = 0;
    protected float y = 0;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
